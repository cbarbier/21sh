#include "21sh.h"

int				go_home(t_21sh *e)
{
	ft_fprintf(e->ttyfd, "function go home\n");
	e->n = 0;
	if (e->beg_sel != -2)
	{
		e->end_sel = 0;
		return (0);
	}
	return (0);
}
