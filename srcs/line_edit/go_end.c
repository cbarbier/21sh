#include "21sh.h"

int				go_end(t_21sh *e)
{
	ft_fprintf(e->ttyfd, "function go end\n");
	e->n = e->ln;
	if (e->beg_sel != -2)
	{
		e->end_sel = e->ln;
		return (0);
	}
	return (0);
}
