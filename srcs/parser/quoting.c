#include "21sh.h"

int					quoting(t_list *l)
{
	int				i;
	t_input			*in;

	i = 0;
	while (l)
	{
		in = (t_input *)(l->content);
		if (in->c == '"')
			i++;
		l = l->next;
	}
	return (i % 2);
}
